/* For loops */

// for loops contain 4 components:

// initialization (let i = 0;)
// test condition (i < 10;)
// final expression or incrementor (i++)
// block (console.log(i))


// You must tell the for loop where to start, where to end, and what number to go up each time
// Counter variable, the limit, then increase counter variable

for (let i = 0; i <= 10; i += 1) {
    console.log(i)
    // You can do more here
}

